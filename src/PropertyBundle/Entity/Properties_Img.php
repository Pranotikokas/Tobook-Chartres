<?php

namespace PropertyBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Properties_Img
 *
 * @ORM\Table(name="properties_img")
 * @ORM\Entity(repositoryClass="PropertyBundle\Repository\Properties_ImgRepository")
 */
class Properties_Img
{
    /**
     * @var int
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var int
     *
     * @ORM\ManyToOne(targetEntity="PropertyBundle\Entity\Properties")
     * @ORM\JoinColumn(name="property_id", referencedColumnName="id")
     */
    private $propertyId;

    /**
     * @var string
     *
     * @ORM\Column(name="path", type="string", length=255)
     */
    private $path;


    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set propertyId
     *
     * @param integer $propertyId
     *
     * @return Properties_Img
     */
    public function setPropertyId($propertyId)
    {
        $this->propertyId = $propertyId;

        return $this;
    }

    /**
     * Get propertyId
     *
     * @return int
     */
    public function getPropertyId()
    {
        return $this->propertyId;
    }

    /**
     * Set path
     *
     * @param string $path
     *
     * @return Properties_Img
     */
    public function setPath($path)
    {
        $this->path = $path;

        return $this;
    }

    /**
     * Get path
     *
     * @return string
     */
    public function getPath()
    {
        return $this->path;
    }
}

